    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
         try {
            int row = jTable1.rowAtPoint(evt.getPoint());
            String hari_tabel = jTable1.getValueAt(row, 0).toString();
            String nama_menu = jTable1.getValueAt(row, 1).toString();
            String jumlahKalori_tabel = jTable1.getValueAt(row, 2).toString();

            hari.setText(hari_tabel);
            namaMenu.setText(nama_menu);
            jumlahKalori.setText(jumlahKalori_tabel);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

