    public void tampil_database() {
        Object[] baris = {"Hari", "Nama Menu", "Jumlah Kalori"};
        tabmode = new DefaultTableModel(null, baris);
        jTable1.setModel(tabmode);
        try {
            hasil = app.tampilData();
            while (hasil.next()) {
                app.setJadwalHariMenuCatering(hasil.getString("hari"));
                app.setNamaMenuCatering(hasil.getString("nama"));
                app.setJumlahKaloriMenuCatering(hasil.getString("jumlahKalori"));

                String[] data = {app.getJadwalHariMenuCatering(), app.getNamaMenuCatering(), app.getJumlahKaloriMenuCatering()};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }

