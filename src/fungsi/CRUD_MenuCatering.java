public class CRUD_MenuCatering extends CRUD{
    private String namaMenuCatering, jumlahKaloriMenuCatering, jadwalHariMenuCatering;
    
    private String hargaLanggananSeminggu, hargaLanggananSebulan;

    public String getNamaMenuCatering() {
        return namaMenuCatering;
    }

    public void setNamaMenuCatering(String namaMenuCatering) {
        this.namaMenuCatering = namaMenuCatering;
    }

    public String getJumlahKaloriMenuCatering() {
        return jumlahKaloriMenuCatering;
    }

    public void setJumlahKaloriMenuCatering(String jumlahKaloriMenuCatering) {
        this.jumlahKaloriMenuCatering = jumlahKaloriMenuCatering;
    }

    public String getJadwalHariMenuCatering() {
        return jadwalHariMenuCatering;
    }

    public void setJadwalHariMenuCatering(String jadwalHariMenuCatering) {
        this.jadwalHariMenuCatering = jadwalHariMenuCatering;
    }

    public String getHargaLanggananSeminggu() {
        return hargaLanggananSeminggu;
    }

    public void setHargaLanggananSeminggu(String hargaLanggananSeminggu) {
        this.hargaLanggananSeminggu = hargaLanggananSeminggu;
    }

    public String getHargaLanggananSebulan() {
        return hargaLanggananSebulan;
    }

    public void setHargaLanggananSebulan(String hargaLanggananSebulan) {
        this.hargaLanggananSebulan = hargaLanggananSebulan;
    }


 
}
